interface I18nStore {
  locale: Locale
  setLocale: (locale: Locale) => void
  t: (key: string, params?: Record<string, string | number>) => string
}

function translateText(text: string, locale: Locale): string {
  const translated = translations[locale][text]
  if (translated !== undefined) return translated
  const zhText = translations['zh-CN'][text]
  return zhText !== undefined ? zhText : text
}

export const useI18n = create<I18nStore>((set, get) => ({
  locale: 'zh-CN',
  setLocale: (locale) => set({ locale }),
  t: (key, params) => {
    const locale = get().locale
    let text = translations[locale][key]
    if (text === undefined) {
      text = translations['zh-CN'][key] || key
    }
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v))
      })
    }
    return text
  },
}))

export function setI18nLocale(locale: Locale) {
  useI18n.getState().setLocale(locale)
}
